#!/usr/bin/env python3
"""Provenance / verification generator for all experiments.

Implements the observability + attribution + reproducibility criteria of
Mo (ICML 2026), "The Age of AI Agents Demands a New Scientific Paradigm to
Sustain Trustworthy Science", for a repository whose experiments are AI-agent
generated. This generator is itself the reproducible "thing": re-running it
regenerates every card from the committed files, so provenance never drifts.

For each experiments/<name>/ it writes experiments/<name>/PROVENANCE.md, and it
emits a machine-readable manifest (docs/verification.json, mirrored into the
site) plus a human index (docs/verification.md).

Run:  python scripts/gen_provenance.py
"""

from __future__ import annotations

import json
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
EXP = ROOT / "experiments"
PAPERS = ROOT / "papers"
DOCS = ROOT / "docs"
SITE = ROOT / "sites" / "reafference_attribution"

ATTRIBUTION = (
    "Human author & director: Jawaun Brown. "
    "Experiment code, sweeps, and paper drafts generated by an AI coding agent "
    "(Claude Code) under the author's direction and review."
)

RUN_PATTERNS = [
    re.compile(r"(doppler[^\n`]*modal run[^\n`]*)", re.I),
    re.compile(r"(python3?\s+-m\s+experiments\.[^\n`]*)"),
    re.compile(r"(python3?\s+experiments/[^\n`]*\.py[^\n`]*)"),
    re.compile(r"(python3?\s+[A-Za-z0-9_./-]+\.py[^\n`]*)"),
]
SEED_PATTERNS = [
    re.compile(r"--base-?seed[ =]+(\d{4,})"),
    re.compile(r"base_seed[^\d]{0,8}(\d{4,})"),
    re.compile(r"\bseed[ =:]+(\d{4,})"),
]
GATE_HINTS = re.compile(r"(pass|gate|confirmed|rho\s*=|r\s*=|specificity|betti|✅|❌|Wilson)", re.I)


def read(p: Path) -> str:
    try:
        return p.read_text(errors="ignore")
    except Exception:
        return ""


def first_match(patterns, *texts):
    for t in texts:
        for pat in patterns:
            m = pat.search(t)
            if m:
                return m.group(1).strip()
    return None


def experiment_dirs():
    return sorted(d for d in EXP.iterdir() if d.is_dir())


def collect(d: Path) -> dict:
    name = d.name
    readme = d / "README.md"
    readme_txt = read(readme)
    results = sorted((d / "results").glob("*.md")) if (d / "results").exists() else []
    latest_result = read(results[-1]) if results else ""
    modal_scripts = sorted(d.glob("modal_*.py"))
    py_scripts = sorted(p for p in d.glob("*.py") if p.name != "__init__.py")
    modal_txt = read(modal_scripts[0]) if modal_scripts else ""
    py_txt = "\n".join(read(p) for p in py_scripts[:6])

    paper = PAPERS / name / "paper.md"
    prereg = PAPERS / name / "preregistration.md"

    run_cmd = first_match(RUN_PATTERNS, readme_txt, modal_txt, latest_result, py_txt)
    seed = first_match(SEED_PATTERNS, modal_txt, py_txt, readme_txt)

    # verification signal: pull the most gate-like lines from the latest result report
    gate_lines = []
    for line in latest_result.splitlines():
        s = line.strip().strip("|").strip()
        if s and GATE_HINTS.search(s) and len(s) < 160 and not s.startswith("#"):
            gate_lines.append(s)
        if len(gate_lines) >= 4:
            break

    status = "paper" if paper.exists() else ("results" if results else "scaffold")
    return dict(
        name=name,
        status=status,
        attribution=ATTRIBUTION,
        run_command=run_cmd,
        seed=seed,
        n_result_reports=len(results),
        result_reports=[str(r.relative_to(ROOT)) for r in results],
        n_modal_entrypoints=len(modal_scripts),
        n_scripts=len(py_scripts),
        paper=str(paper.relative_to(ROOT)) if paper.exists() else None,
        preregistration=str(prereg.relative_to(ROOT)) if prereg.exists() else None,
        verification_signals=gate_lines,
        artifacts_committed=[str(r.relative_to(ROOT)) for r in results]
        + ([str(paper.relative_to(ROOT))] if paper.exists() else []),
        artifacts_local="artifacts/ (gitignored raw outputs; see result reports for summaries)",
        regen=f"python scripts/regen.py {name}",
    )


def card_md(e: dict) -> str:
    def val(x):
        return x if x else "_(not auto-detected — see README/result reports)_"

    lines = [
        f"# Provenance — `{e['name']}`",
        "",
        "> Auto-generated by `scripts/gen_provenance.py` (do not edit by hand). "
        "Implements the observability / attribution / reproducibility criteria of "
        "Mo (ICML 2026) for AI-agent-generated science.",
        "",
        f"- **Status:** {e['status']}",
        f"- **Attribution:** {e['attribution']}",
        f"- **Run command:** `{val(e['run_command'])}`" if e['run_command'] else
        f"- **Run command:** {val(e['run_command'])}",
        f"- **Seed:** {val(e['seed'])}",
        f"- **Reproduce (one command):** `{e['regen']}`",
        "",
        "## Verification",
        f"- Pre-registration: {('`'+e['preregistration']+'`') if e['preregistration'] else '_none_'}",
        f"- Result reports ({e['n_result_reports']}):"
        + ("" if e['result_reports'] else " _none committed_"),
    ]
    for r in e["result_reports"]:
        lines.append(f"  - `{r}`")
    if e["verification_signals"]:
        lines += ["", "Key verification signals (from the latest result report):"]
        for g in e["verification_signals"]:
            lines.append(f"  - {g}")
    lines += [
        "",
        "## Artifacts",
        f"- Committed: {', '.join('`'+a+'`' for a in e['artifacts_committed']) if e['artifacts_committed'] else '_code only_'}",
        f"- Local-only: {e['artifacts_local']}",
        f"- Paper: {('`'+e['paper']+'`') if e['paper'] else '_none_'}",
        "",
    ]
    return "\n".join(lines)


def main():
    manifest = []
    for d in experiment_dirs():
        e = collect(d)
        (d / "PROVENANCE.md").write_text(card_md(e))
        manifest.append(e)

    DOCS.mkdir(exist_ok=True)
    payload = dict(
        kind="experiment verification manifest",
        criteria="observability + attribution + reproducibility (Mo, ICML 2026)",
        attribution=ATTRIBUTION,
        n_experiments=len(manifest),
        experiments=manifest,
    )
    (DOCS / "verification.json").write_text(json.dumps(payload, indent=2) + "\n")
    if SITE.exists():
        (SITE / "verification.json").write_text(json.dumps(payload, indent=2) + "\n")

    # human index
    by_status = {}
    for e in manifest:
        by_status.setdefault(e["status"], []).append(e)
    idx = [
        "# Verification & Provenance Index",
        "",
        "Auto-generated by `scripts/gen_provenance.py`. Every experiment carries a "
        "`PROVENANCE.md` card (attribution, run command, seed, verification signals, "
        "artifacts, one-command regen), per Mo (ICML 2026)'s observability / attribution "
        "/ reproducibility criteria for AI-agent-generated science.",
        "",
        f"**Attribution.** {ATTRIBUTION}",
        "",
        f"**{len(manifest)} experiments** — "
        + ", ".join(f"{k}: {len(v)}" for k, v in sorted(by_status.items())),
        "",
        "| Experiment | Status | Reports | Paper | Provenance |",
        "| --- | --- | ---: | :--: | --- |",
    ]
    for e in sorted(manifest, key=lambda x: (x["status"] != "paper", x["name"])):
        paper = "✓" if e["paper"] else "—"
        idx.append(
            f"| `{e['name']}` | {e['status']} | {e['n_result_reports']} | {paper} | "
            f"`experiments/{e['name']}/PROVENANCE.md` |"
        )
    idx.append("")
    (DOCS / "verification.md").write_text("\n".join(idx))
    print(f"[provenance] wrote {len(manifest)} PROVENANCE.md cards + "
          f"docs/verification.{{json,md}} + site manifest")


if __name__ == "__main__":
    main()
