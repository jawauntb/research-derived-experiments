#!/usr/bin/env python3
# pyright: reportMissingImports=false
"""Build the semantic selection-control SCG paper PDF."""

from __future__ import annotations

import argparse
import json
from pathlib import Path
import sys
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / "scripts"))
import paperkit as pk  # noqa: E402

from experiments.structure_compatible_generalization.semantic_selection_control import (  # noqa: E402
    selection_records_from_dicts,
)
from experiments.structure_compatible_generalization.summarize_semantic_selection_control import (  # noqa: E402
    SELECTOR_ORDER,
    semantic_selection_summary,
    write_figures,
)
from reportlab.platypus import PageBreak  # noqa: E402


def _fmt(value: float | None, digits: int = 3) -> str:
    if value is None:
        return "n/a"
    return f"{value:.{digits}f}"


def _load(payload_path: Path) -> tuple[dict[str, Any], dict[str, Any]]:
    payload = json.loads(payload_path.read_text())
    return payload, semantic_selection_summary(payload)


def _ordered_selector_rows(summary: dict[str, Any]) -> list[dict[str, Any]]:
    order = {selector: idx for idx, selector in enumerate(SELECTOR_ORDER)}
    return sorted(
        summary["by_selector"],
        key=lambda row: order.get(str(row["selector"]), 999),
    )


def build(payload_path: Path, out: Path, figure_dir: Path) -> None:
    payload, summary = _load(payload_path)
    records = selection_records_from_dicts(payload["selection_records"])
    figure_paths = write_figures(records, summary, figure_dir)

    p = pk.Paper(str(out), str(figure_dir))
    p.title("Semantic Selection Control for Structure-Compatible Generalization")
    p.authors("Jawaun Brown")
    p.authors("Phase 6: OOD-free frozen-encoder model selection")
    p.rule()
    p.abstract(
        "This phase turns semantic retrieval compatibility into an OOD-free "
        "model-selection protocol. Candidate retrieval models are grouped into "
        "encoder-threshold seed zoos, filtered to high train and ID validation "
        "performance, selected by learned compatibility or baseline selectors, "
        "and evaluated only afterward on held-out semantic variants."
    )

    manifest = payload.get("manifest", {})
    p.h1("1. Manifest")
    p.table(
        [
            ["Field", "Value"],
            ["GPU", str(manifest.get("gpu", "n/a"))],
            ["encoders", str(manifest.get("encoder_keys", "n/a"))],
            ["thresholds", str(manifest.get("thresholds", "n/a"))],
            ["zoos", str(manifest.get("n_zoos", "n/a"))],
            ["configs per zoo", str(manifest.get("configs_per_zoo", "n/a"))],
            ["selection records", str(summary["n_records"])],
        ],
        caption="Table 1. Semantic selection Modal L4 manifest.",
        col_widths=[150, 320],
    )

    p.h1("2. Gate Status")
    gate_rows = [["Gate", "Passed"]]
    for key, value in summary["gates"].items():
        gate_rows.append([key, str(bool(value))])
    p.table(
        gate_rows,
        caption="Table 2. Pre-registered selection-control gates.",
        col_widths=[300, 85],
    )

    selector_rows = [
        ["Selector", "Zoos", "OOD", "Regret", "Lift", "Ties"],
    ]
    for item in _ordered_selector_rows(summary):
        selector_rows.append(
            [
                item["selector"],
                str(int(item["n_zoos"])),
                _fmt(item["mean_selected_ood"]),
                _fmt(item["mean_regret"]),
                _fmt(item["mean_lift_vs_random"]),
                _fmt(item["mean_tied_count"]),
            ]
        )
    p.table(
        selector_rows,
        caption="Table 3. Selector performance under train/ID equivalence.",
        col_widths=[180, 45, 65, 65, 65, 45],
    )

    p.flow.append(PageBreak())
    for idx, fig_path in enumerate(figure_paths, start=1):
        caption = (
            "Figure 1. Mean selected OOD by OOD-free selector."
            if idx == 1
            else "Figure 2. Selection regret versus OOD oracle."
        )
        p.figure(str(fig_path), caption, width_in=6.1)

    p.h1("3. Scope")
    p.para(
        "The protocol selects among finite semantic retrieval candidates "
        "generated by public frozen encoders. It is an OOD-certifiability-lite "
        "result for this structured setting, not a full behavioral guarantee "
        "for open-ended language systems."
    )
    p.build()


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--in", dest="input", type=Path, required=True)
    parser.add_argument(
        "--out",
        type=Path,
        default=Path(
            "papers/structure_compatible_generalization/"
            "semantic_selection_control.pdf"
        ),
    )
    parser.add_argument(
        "--figure-dir",
        type=Path,
        default=Path("papers/structure_compatible_generalization/figures"),
    )
    args = parser.parse_args()
    args.out.parent.mkdir(parents=True, exist_ok=True)
    build(args.input, args.out, args.figure_dir)
    print(f"Wrote {args.out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
