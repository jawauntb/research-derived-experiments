#!/usr/bin/env python3
# pyright: reportMissingImports=false
"""Build the structure-compatible generalization paper PDF.

Input is the JSON payload from
`experiments/structure_compatible_generalization/modal_l4_suite.py`.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
import sys
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / "scripts"))
import paperkit as pk  # noqa: E402

from experiments.structure_compatible_generalization.core import (  # noqa: E402
    rows_from_records,
    summarize_rows,
)
from experiments.structure_compatible_generalization.summarize_suite import (  # noqa: E402
    write_figures,
)


def _fmt(value: float | None, digits: int = 3) -> str:
    if value is None:
        return "n/a"
    return f"{value:.{digits}f}"


def _load_summary(payload_path: Path) -> tuple[dict[str, Any], dict[str, Any]]:
    payload = json.loads(payload_path.read_text())
    summary = summarize_rows(rows_from_records(payload["rows"]))
    return payload, summary


def build(payload_path: Path, out: Path, figure_dir: Path) -> None:
    payload, summary = _load_summary(payload_path)
    figure_paths = write_figures(summary, figure_dir)

    p = pk.Paper(str(out), str(figure_dir))
    p.title("Structure-Compatible Generalization")
    p.authors("Jawaun Brown")
    p.authors("Predicting OOD behavior from preserved transformation structure")
    p.rule()
    p.abstract(
        "Modern learning systems are underspecified: many finite models can fit "
        "the same training data and pass in-distribution checks while behaving "
        "differently under deployment shifts. This paper reports a controlled "
        "diagnostic suite testing whether the transformation structure a learned "
        "function preserves predicts OOD behavior better than loss, validation, "
        "norm, or sharpness proxies. The suite combines cyclic symbolic MLPs, "
        "rotated visual objects, and a modular algorithmic table task under one "
        "common schema. The claim is bounded: for finite domains where "
        "deployment shift is generated by a known transformation family, "
        "compatibility is an OOD diagnostic and model-selection signal, not a "
        "universal certification theorem."
    )

    p.h1("1. Underspecification Target")
    p.para(
        "The practical question is which model to trust when train loss and "
        "ID validation do not identify the transportable rule. The diagnostic "
        "tested here measures compatibility with transformations expected to "
        "generate deployment cases, then compares that signal with standard "
        "selection proxies."
    )

    p.h1("2. Suite")
    p.para(
        "The suite reuses the cyclic symbolic and rotated vision weakness "
        "benchmarks and adds a modular addition task. Each model emits one "
        "common row: train accuracy, ID validation accuracy, OOD accuracy, true "
        "compatibility, wrong-group compatibility, loss, parameter norm, and "
        "sharpness when available."
    )
    manifest = payload.get("manifest", {})
    p.table(
        [
            ["Field", "Value"],
            ["domains", ", ".join(manifest.get("domains", []))],
            ["GPU", str(manifest.get("gpu", "n/a"))],
            ["symbolic models", str(manifest.get("symbolic_models", "n/a"))],
            ["vision models", str(manifest.get("vision_models", "n/a"))],
            ["modular models", str(manifest.get("modular_models", "n/a"))],
            ["rows", str(summary["n_rows"])],
        ],
        caption="Table 1. Suite manifest.",
        col_widths=[140, 330],
    )

    p.h1("3. Predictor Rankings")
    rows = [["Domain", "Top predictor", "Pearson r", "Mean OOD"]]
    for domain, domain_summary in summary["by_domain"].items():
        top = domain_summary["predictor_ranking"][0]
        rows.append(
            [
                domain,
                top["predictor"],
                _fmt(top["pearson"]),
                _fmt(domain_summary["mean_ood_accuracy"]),
            ]
        )
    p.table(rows, caption="Table 2. Top OOD predictors by domain.", col_widths=[120, 170, 90, 90])

    if figure_paths:
        p.figure(
            str(figure_paths[0]),
            "Figure 1. Predictor correlations by domain. True compatibility is "
            "compared against wrong-group compatibility and ID validation.",
            width_in=6.0,
        )
    if len(figure_paths) > 1:
        p.figure(
            str(figure_paths[1]),
            "Figure 2. OOD-free model selection. Models are selected by each "
            "proxy among train/ID-equivalent candidates and evaluated only "
            "after selection.",
            width_in=6.0,
        )

    p.h1("4. Selection Without OOD Labels")
    selection = summary["selection"]["aggregate"]
    sel_rows = [["Predictor", "Domains", "Mean selected OOD"]]
    for predictor, stats in sorted(
        selection.items(),
        key=lambda item: item[1]["mean_selected_ood"],
        reverse=True,
    )[:8]:
        sel_rows.append(
            [
                predictor,
                str(int(stats["domains"])),
                _fmt(stats["mean_selected_ood"]),
            ]
        )
    p.table(sel_rows, caption="Table 3. Selection by proxy among ID-equivalent models.", col_widths=[210, 90, 150])

    p.h1("5. Operating Regime")
    p.para(
        "This is not full OOD certification. The allowed claim is narrower: "
        "for finite or structured domains where deployment shift is generated "
        "by a candidate transformation family, measured compatibility can "
        "predict and improve model selection under underspecification."
    )
    p.para(
        "The next regime transition is learned or weakly inferred "
        "transformation discovery, followed by compatibility-guided "
        "regularization and active data acquisition."
    )
    p.build()


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--in", dest="input", type=Path, required=True)
    parser.add_argument(
        "--out",
        type=Path,
        default=Path("papers/structure_compatible_generalization/paper.pdf"),
    )
    parser.add_argument(
        "--figure-dir",
        type=Path,
        default=Path("papers/structure_compatible_generalization/figures"),
    )
    args = parser.parse_args()
    args.out.parent.mkdir(parents=True, exist_ok=True)
    build(args.input, args.out, args.figure_dir)
    print(f"Wrote {args.out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
