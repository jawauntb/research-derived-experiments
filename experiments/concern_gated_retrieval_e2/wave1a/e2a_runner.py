"""COGR-E2a per-episode runner (Wave 1a scaffold).

Wave 1a runs one confirmatory episode per ``(condition, seed, family)``
cell. This module supplies the per-episode runner that composes the
frozen Wave 0 primitives — the sealed environment, the logged
epsilon-greedy probe policy, and the off-policy concern-update rule —
against one condition drawn from :mod:`wave1a.conditions`.

The runner is scaffolding: it fixes the receipt shape and the
single-shot sealed-env contract, but Wave 1a experiment logic (the
paired-seed variance estimator, the specificity check against Wave 0
info-matched second signals, the per-family threshold decision) belongs
to sibling modules. This module intentionally exposes exactly one
public entrypoint plus the frozen result dataclass; nothing else.

Reuse boundary
--------------

* :class:`~experiments.concern_gated_retrieval_e2.wave0.sealed_env.SealedEnvironment`
  — single-shot sealed environment. Wave 1a constructs it in
  ``mode="confirmatory"`` for confirmatory-family episodes and
  ``mode="calibration"`` for calibration-family replays only (see
  ``PREREGISTRATION.md`` §5.2 propensity accounting; calibration replays
  are permitted only inside the coverage-audit / propensity gates).
* :class:`~experiments.concern_gated_retrieval_e2.wave0.concern_update.LoggedProbePolicy`
  — epsilon-greedy wrapper whose selection propensity is exactly the
  quantity IPS and DR estimators divide by. Wave 1a locks
  ``epsilon = DEFAULT_EPSILON`` from Wave 0 by default; the replay knob
  in ``PREREGISTRATION.md`` §7 permits up to ``epsilon = 0.10`` on a
  §5.1 coverage failure.
* :func:`~experiments.concern_gated_retrieval_e2.wave0.concern_update.update_concern`
  — off-policy concern update. Wave 1a passes the condition's
  ``update_rule`` (``"ips"`` or ``"dr"``) as the ``estimator``. Frozen
  conditions (``FROZEN_WRONG``, ``ORACLE_CEILING``, ``SHUFFLED``,
  ``WRONG_AGENT``) skip the update.

Anti-leakage
------------

The runner is the single choke point where sealed-side condition
factories meet policy-side probe policies. It:

1. Runs the condition's ``initial_concern_factory`` against the sealed
   :class:`EpisodeSpec` *before* constructing the sealed environment
   observer view. The evaluator-side factory is the only place that
   dereferences sealed fields; its numeric output is what the policy
   sees.
2. Constructs a
   :class:`~experiments.concern_gated_retrieval_e2.wave0.sealed_env.SealedEnvironment`,
   calls ``observe()`` (any number of times is safe; the view is
   stateless), and never lets the sealed :class:`EpisodeSpec` reach the
   nomination policy or the :class:`LoggedProbePolicy` wrapper.
3. Calls ``evaluate()`` exactly once per episode. The single-shot rule
   is enforced by the sealed environment itself
   (``SealedEvaluationError`` on a second call); the runner records the
   call count on :attr:`E2aEpisodeResult.sealed_env_evaluate_calls` so
   downstream receipts can regression-check the invariant.
4. Applies :class:`IntegrityAudit` to the *policy* nomination callable
   before wrapping it in :class:`LoggedProbePolicy`. The condition's
   factory is not passed through the audit — its evaluator-side reads
   are legitimate — but any policy code the runner constructs is.

Wave 1a scope
-------------

This runner CAN reject the update rule (via the fatal gates its receipts
feed). It CANNOT establish learned memory geometry (Wave 1b / COGR-E2b),
the L1 dual-source-retrieval mechanism claim (Wave 1b), or the L2
history-derived-concern-recovery claim (also Wave 1b). Wave 1a's
promotable output is a screen decision — PASS opens Wave 1b; KILL kills
the update rule as written and forbids Wave 1b from reusing it without
a redesigned preregistration (honor-the-preregistration rule).
"""

from __future__ import annotations

import random
from dataclasses import dataclass, field
from typing import Callable, Mapping, Sequence

from experiments.concern_gated_retrieval_e2.wave0.concern_update import (
    DEFAULT_EPSILON,
    LoggedProbePolicy,
    NominationPolicy,
    ProbeReceipt,
    update_concern,
)
from experiments.concern_gated_retrieval_e2.wave0.sealed_env import (
    EpisodeContext,
    EpisodeSpec,
    IntegrityAudit,
    RetrievalChoice,
    SealedEnvironment,
    SealedMode,
    SealedOutcome,
)
from experiments.concern_gated_retrieval_e2.wave1a.conditions import Condition

__all__ = [
    "DEFAULT_BUDGET_FLOOR",
    "E2aEpisodeResult",
    "run_e2a_episode",
]


#: Minimum retrieval-decision budget the runner submits to the sealed
#: environment. Wave 1a's confirmatory episodes are generated by the
#: Wave 0 family generators, each of which sets its own ``budget``; the
#: runner never overrides it. This floor exists as a defensive check so
#: an episode with an accidentally-zero budget is rejected early.
DEFAULT_BUDGET_FLOOR: int = 1


@dataclass(frozen=True)
class E2aEpisodeResult:
    """One Wave 1a per-episode receipt.

    Emitted by :func:`run_e2a_episode`. Every field is a policy-visible
    quantity or a numeric derivative thereof; no sealed
    :class:`EpisodeSpec` fields ride on this dataclass.

    Attributes
    ----------
    episode_id:
        The sealed episode id, copied through from
        :attr:`EpisodeSpec.episode_id`. Wave 1a receipts join on this key
        against the paired-seed variance estimator.
    condition_name:
        The name of the :class:`Condition` this row was scored under.
    family:
        The procedural family the episode was drawn from
        (``delayed_commitments`` / ``maintenance_fault`` /
        ``resource_constrained``). Copied from
        :attr:`EpisodeSpec.family`.
    template_family_split:
        ``"calibration"`` or ``"confirmatory"``. Copied through so
        downstream aggregators can refuse a cross-split batch at the
        update boundary (Wave 0 ``update_concern`` guard).
    rng_seed:
        The caller-supplied PRNG seed used to drive the
        :class:`LoggedProbePolicy` exploration decision. Together with
        ``episode_id`` this makes the row deterministic and replayable.
    concern_before:
        The numeric concern prior emitted by the condition's
        ``initial_concern_factory``. A frozen copy so downstream
        aggregators cannot mutate it.
    concern_after:
        The updated concern prior after
        :func:`~experiments.concern_gated_retrieval_e2.wave0.concern_update.update_concern`
        was applied. ``None`` when the condition's ``update_rule`` is
        ``None`` (frozen-wrong, oracle, shuffled, wrong-agent).
    receipt:
        The :class:`ProbeReceipt` produced by
        :meth:`LoggedProbePolicy.select`. Carries the logging-policy
        propensity every off-policy estimator divides by.
    decision:
        The :class:`RetrievalChoice` submitted to the sealed environment.
    outcome:
        The :class:`SealedOutcome` returned by the single ``evaluate()``
        call.
    sealed_env_evaluate_calls:
        The number of times the runner called ``SealedEnvironment.evaluate``
        on this episode. Frozen at ``1`` on a successful run; the field
        exists so downstream receipts can regression-check the
        single-shot invariant.
    integrity_audit_passed:
        ``True`` iff :meth:`IntegrityAudit.assert_clean` passed on the
        policy nomination callable the runner used. A ``False`` value
        cannot appear on a successful run — an audit failure would raise
        before the result was constructed.
    """

    episode_id: str
    condition_name: str
    family: str
    template_family_split: str
    rng_seed: int
    concern_before: Mapping[str, float]
    concern_after: Mapping[str, float] | None
    receipt: ProbeReceipt
    decision: RetrievalChoice
    outcome: SealedOutcome
    sealed_env_evaluate_calls: int = 1
    integrity_audit_passed: bool = True

    #: Reserved for future receipts. Kept empty in Wave 1a scaffolding so
    #: the paired-seed variance estimator can bolt on family-level
    #: diagnostics without a shape change.
    extras: Mapping[str, float] = field(default_factory=dict)


# --------------------------------------------------------------------------- #
# Policy factory (scaffold)
# --------------------------------------------------------------------------- #


def _concern_biased_ranker(concern: Mapping[str, float]) -> NominationPolicy:
    """Return a nomination policy that ranks candidates by concern weight.

    Scaffold. Wave 1a's real ranker is the Wave 0 rarity-corrected
    multiplicative fusion, imported by the sweep runner from
    ``wave0.baselines``. This helper is what the per-episode runner
    wraps in :class:`LoggedProbePolicy` when no external ranker is
    supplied; it only reads the policy-visible :class:`EpisodeContext`
    and the numeric concern prior, and it never dereferences sealed
    fields. That keeps the wrapped callable
    :meth:`IntegrityAudit.assert_clean`-clean by construction.
    """
    # Copy so the closure does not share state with the caller.
    concern_snapshot = dict(concern)

    def rank(context: EpisodeContext) -> Sequence[str]:
        # Rank candidates by concern weight (desc), tie-break by node id
        # for determinism. Concern-blind candidates fall through to a
        # deterministic tail.
        return tuple(
            sorted(
                context.candidate_nodes,
                key=lambda node: (-float(concern_snapshot.get(node, 0.0)), node),
            )
        )

    return rank


# --------------------------------------------------------------------------- #
# Main entrypoint
# --------------------------------------------------------------------------- #


def _sealed_mode_for(episode: EpisodeSpec) -> SealedMode:
    """Return the sealed-env mode consistent with ``episode``.

    Wave 1a's confirmatory sweep runs on
    ``template_family_split == "confirmatory"`` episodes;
    calibration-family replays (the coverage / propensity gates in
    ``PREREGISTRATION.md`` §7) run on ``"calibration"`` episodes. The
    sealed environment refuses the mismatched pairing, so mirroring the
    split into the mode keeps a mis-tagged episode from silently
    entering the wrong pipeline.
    """
    if episode.template_family_split == "confirmatory":
        return "confirmatory"
    return "calibration"


def run_e2a_episode(
    episode: EpisodeSpec,
    condition: Condition,
    rng_seed: int,
    *,
    nomination_factory: (
        Callable[[Mapping[str, float]], NominationPolicy] | None
    ) = None,
    epsilon: float = DEFAULT_EPSILON,
    source_id: str = "trusted",
) -> E2aEpisodeResult:
    """Run one Wave 1a per-episode receipt for ``episode`` under ``condition``.

    Parameters
    ----------
    episode:
        The sealed :class:`EpisodeSpec` the confirmatory sweep drew.
        The runner does not fork or mutate it; the sealed environment
        strips the sealed fields before any policy view is returned.
    condition:
        A :class:`Condition` from :mod:`wave1a.conditions`. The
        condition's ``initial_concern_factory`` runs against the sealed
        episode; the ``update_rule`` (``"ips"`` / ``"dr"`` / ``None``)
        drives the post-outcome ``update_concern`` call.
    rng_seed:
        Caller-supplied PRNG seed. Wave 1a is paired at the seed level
        across conditions; the runner does not translate this into a
        sealed-env observe seed.
    nomination_factory:
        Optional caller-supplied nomination-policy factory. Signature is
        ``(concern_prior) -> NominationPolicy``. Defaults to
        :func:`_concern_biased_ranker`, the scaffold ranker. Wave 1a's
        sweep will pass in the rarity-corrected multiplicative fusion
        from ``wave0.baselines``.
    epsilon:
        Exploration probability for :class:`LoggedProbePolicy`. Defaults
        to Wave 0's :data:`DEFAULT_EPSILON` (``0.05``). The wrapper
        constructor refuses ``epsilon <= 0``; that is the exploration-
        coverage floor recorded in ``PREREGISTRATION.md`` §5.1.
    source_id:
        Provenance id passed through to :class:`ProbeReceipt`. Wave 1a
        confirmatory sweeps use ``"trusted"`` — Wave 0 §4.4's poisoning
        guard requires a distinct id per untrusted source, and the Wave
        1a preregistration explicitly out-of-scopes untrusted-source
        stress (``PREREGISTRATION.md`` §5.6).

    Returns
    -------
    E2aEpisodeResult
        A frozen per-episode receipt whose ``sealed_env_evaluate_calls``
        is ``1`` on success.

    Raises
    ------
    ValueError, TypeError:
        Propagated from the Wave 0 constructors on malformed input.
    SealedEvaluationError:
        Propagated from the sealed environment on a contract violation
        (a caller who somehow triggers a second ``evaluate()`` call, an
        out-of-candidate selection, or a budget overrun).
    LeakageError:
        Raised by :meth:`IntegrityAudit.assert_clean` if a caller-
        supplied nomination policy dereferences a sealed field.
    """
    if not isinstance(episode, EpisodeSpec):
        raise TypeError("episode must be an EpisodeSpec")
    if not isinstance(condition, Condition):
        raise TypeError("condition must be a Condition")
    if not isinstance(rng_seed, int) or isinstance(rng_seed, bool):
        raise TypeError("rng_seed must be a non-boolean int")
    if episode.budget < DEFAULT_BUDGET_FLOOR:
        raise ValueError(
            f"episode budget must be >= {DEFAULT_BUDGET_FLOOR}; got "
            f"{episode.budget}"
        )

    # 1. Evaluator-side concern-prior construction. The condition's
    #    factory may legitimately dereference sealed fields; its output
    #    is what the policy sees.
    concern_before: dict[str, float] = dict(
        condition.initial_concern_factory(episode)
    )

    # 2. Build the policy-visible nomination callable and audit it.
    factory = nomination_factory or _concern_biased_ranker
    nomination = factory(concern_before)
    if not callable(nomination):
        raise TypeError(
            "nomination_factory must return a callable NominationPolicy"
        )
    try:
        IntegrityAudit.assert_clean(nomination)
        integrity_ok = True
    except Exception:
        # Bubble the leakage failure — the runner does not swallow it.
        raise

    # 3. Wrap the audited nomination in the logged epsilon-greedy probe
    #    policy. Wave 0's constructor enforces epsilon > 0.
    probe_policy = LoggedProbePolicy(
        nomination, epsilon=epsilon, source_id=source_id
    )

    # 4. Construct the sealed environment in the mode matching the
    #    episode's family split. Observe once for the policy view.
    env = SealedEnvironment(episode, mode=_sealed_mode_for(episode))
    context = env.observe(seed=rng_seed)

    # 5. Draw one logged probe. Wave 1a's confirmatory sweep runs one
    #    probe per episode; multi-probe rollouts remain a Wave 1b object.
    rng = random.Random(rng_seed)
    selected, receipt = probe_policy.select(context, rng)

    # 6. Submit the retrieval decision to the sealed environment
    #    exactly once. The single-shot rule is enforced by the env
    #    itself; the runner records the call on the result for
    #    downstream regression checks.
    decision = RetrievalChoice(selected=(selected,), wall_actions=1)
    outcome = env.evaluate(decision)
    sealed_calls = 1
    assert env.evaluated, "sealed environment must report evaluated=True"

    # 7. If the condition has an update rule, apply
    #    ``update_concern`` to produce the post-outcome prior. Wave 0's
    #    ``update_concern`` refuses to consume confirmatory receipts at
    #    a calibration entry point; the runner cross-runs both split
    #    kinds only through explicit calibration replays.
    if condition.update_rule is not None:
        # Wave 0's ``update_concern`` refuses confirmatory receipts on
        # its calibration path. Wave 1a confirmatory rows use the
        # *confirmatory* update surface, wired through Wave 1a's
        # sibling sweep module. The scaffold here calls the Wave 0
        # API for calibration-tagged receipts only; confirmatory
        # receipts return the unchanged prior as ``concern_after`` and
        # defer the confirmatory-mode update to the sweep runner.
        if receipt.template_family_split == "calibration":
            concern_after: Mapping[str, float] | None = update_concern(
                concern_before,
                (receipt,),
                (outcome,),
                estimator=condition.update_rule,
            )
        else:
            concern_after = dict(concern_before)
    else:
        concern_after = None

    return E2aEpisodeResult(
        episode_id=episode.episode_id,
        condition_name=condition.name,
        family=episode.family,
        template_family_split=episode.template_family_split,
        rng_seed=rng_seed,
        concern_before=concern_before,
        concern_after=concern_after,
        receipt=receipt,
        decision=decision,
        outcome=outcome,
        sealed_env_evaluate_calls=sealed_calls,
        integrity_audit_passed=integrity_ok,
    )
