"""Finite transformation discovery helpers.

These utilities intentionally operate on small finite domains. They are meant
to turn observed input/label pairs into candidate transformation families
without consulting OOD labels.
"""

from __future__ import annotations

from dataclasses import asdict, dataclass
from typing import Iterable


Pair = tuple[int, int]
Table = tuple[int, ...]


@dataclass(frozen=True)
class ShiftEvidence:
    shift: int
    support: int
    violations: int
    admitted: bool

    def to_record(self) -> dict[str, int | bool]:
        return asdict(self)


@dataclass(frozen=True)
class DiscoveredShiftFamily:
    modulus: int
    min_support: int
    evidence: tuple[ShiftEvidence, ...]

    @property
    def admitted_shifts(self) -> tuple[int, ...]:
        return tuple(item.shift for item in self.evidence if item.admitted)

    @property
    def admitted_count(self) -> int:
        return len(self.admitted_shifts)

    @property
    def non_identity_count(self) -> int:
        return sum(shift != 0 for shift in self.admitted_shifts)

    def to_record(self) -> dict[str, object]:
        return {
            "modulus": self.modulus,
            "min_support": self.min_support,
            "admitted_shifts": list(self.admitted_shifts),
            "admitted_count": self.admitted_count,
            "non_identity_count": self.non_identity_count,
            "evidence": [item.to_record() for item in self.evidence],
        }


def translated_pair(pair: Pair, shift: int, modulus: int) -> Pair:
    a, b = pair
    return ((a + shift) % modulus, b)


def infer_supported_shifts(
    train_pairs: Iterable[Pair],
    train_labels: Iterable[int],
    *,
    modulus: int,
    min_support: int,
) -> DiscoveredShiftFamily:
    """Infer label-compatible translations from observed overlaps.

    A non-identity shift is admitted only if at least `min_support` observed
    pairs have their shifted counterpart also observed, and every such overlap
    obeys y(shift(x)) = y(x) + shift mod n. Identity is admitted when it has
    support, which keeps downstream compatibility scores defined.
    """
    observed = {
        pair: int(label) % modulus
        for pair, label in zip(train_pairs, train_labels)
    }
    evidence: list[ShiftEvidence] = []
    for shift in range(modulus):
        support = 0
        violations = 0
        for pair, label in observed.items():
            shifted = translated_pair(pair, shift, modulus)
            if shifted not in observed:
                continue
            support += 1
            expected = (label + shift) % modulus
            if observed[shifted] != expected:
                violations += 1
        admitted = support >= min_support and violations == 0
        evidence.append(
            ShiftEvidence(
                shift=shift,
                support=support,
                violations=violations,
                admitted=admitted,
            )
        )
    if not any(item.admitted for item in evidence):
        identity_support = len(observed)
        evidence[0] = ShiftEvidence(
            shift=0,
            support=identity_support,
            violations=0,
            admitted=identity_support > 0,
        )
    return DiscoveredShiftFamily(
        modulus=modulus,
        min_support=min_support,
        evidence=tuple(evidence),
    )


def close_shift_family(shifts: Iterable[int], modulus: int) -> tuple[int, ...]:
    """Return the cyclic subgroup generated by admitted shifts."""
    closure = {0}
    frontier = {shift % modulus for shift in shifts}
    while frontier:
        shift = frontier.pop()
        if shift in closure:
            continue
        closure.add(shift)
        for known in list(closure):
            candidate = (shift + known) % modulus
            if candidate not in closure:
                frontier.add(candidate)
    return tuple(sorted(closure))


def shift_table_compatibility(
    table: Table,
    *,
    modulus: int,
    shifts: Iterable[int],
) -> float:
    """Measure compatibility of a finite table with a discovered shift set."""
    materialized = tuple(sorted({shift % modulus for shift in shifts}))
    if not materialized:
        return 0.0
    compatible = 0
    pairs = [(a, b) for a in range(modulus) for b in range(modulus)]
    for shift in materialized:
        ok = True
        for a, b in pairs:
            lhs = table[((a + shift) % modulus) * modulus + b]
            rhs = (table[a * modulus + b] + shift) % modulus
            if lhs != rhs:
                ok = False
                break
        compatible += int(ok)
    return compatible / len(materialized)
